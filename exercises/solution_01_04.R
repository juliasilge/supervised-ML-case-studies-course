library(tidyverse)
library(arrow)
cars2018 <- read_parquet("data/cars2018.parquet")

# Deselect the 2 columns to create cars_vars
car_vars <- cars2018 %>%
    select(-model, -model_index)

# Fit a linear model
fit_all <- lm(mpg ~ ., data = car_vars)

# Print the summary of the model
summary(fit_all)